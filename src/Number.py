# -*- coding: utf-8 -*-
#注释
#输出
print('hello, world')
#带有小数部分的数字类型是float
print(8/5)
#除法/永远返回一个浮点数
print(17/3)
#使用除法保留整数结果，丢掉小数部分使用//
print(17//3)
#计算余数%
print(17%3)
#计算幂乘方
print("2的4次方是",2**4)
#等号( '=' )用于给变量赋值。赋值之后，在下一个提示符之前不会有任何结果显示
#变量使用前必须定义
width=20
height=5*9
print(width*height)
#浮点数有完整的支持；整数和浮点数的混合计算中，整数会被转换为浮点数
print(3 * 3.75 / 1.5)
print(7.0/2)